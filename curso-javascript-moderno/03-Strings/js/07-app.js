// Video 22. String Methods - Convertir a Mayusculas o Minusculas

const producto = 'Monitor 20 Pulgadas';

// .toUpperCase pasa todo el texto a MAYÚSCULAS
console.log(producto.toUpperCase());

// .toLowerCase pasa todo el texto a MINÚSCULAS
console.log(producto.toLowerCase());

const email = "CORREO@CORREO.COM";
console.log(email);
console.log(email.toLowerCase());

// .toString convierte una variable de tipo entero a String
const precio = 300;
console.log(precio);
console.log(precio.toString());
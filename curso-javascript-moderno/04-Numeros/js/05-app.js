//Video 27. Incrementos o Decrementos

// Incremento
let puntaje = 5;

puntaje++; //5

++puntaje; //6

//Decremento
let puntajeD = 5;

puntajeD--; //5

--puntajeD; //4

//Incremento con más puntos
let puntajeN = 5;

puntajeN += 3;

//Decremento con más puntos
let puntajeM = 5;

puntajeM -= 3;


